export const obtenerProductos = () => {
  return productosData;
};

export const obtenerProductoPorId = (id) => {
  return productosData.find(p => p.id === id);
};

export const obtenerProductosPorCategoria = (categoria) => {
  return productosData.filter(p => p.categoria === categoria);
};

export const agregarProducto = (producto, usuarioActual = 'admin') => {
  const ahora = new Date().toISOString();
  
  const nuevoProducto = {
    ...producto,
    id: productosData.length + 1,
    
    // Campos de auditoría automáticos
    autor: usuarioActual,
    fechaCreacion: ahora,
    fechaActualizacion: ahora,
    ultimoEditor: usuarioActual
  };
  
  productosData.push(nuevoProducto);
  return nuevoProducto;
};

export const actualizarProducto = (id, datosActualizados, usuarioActual = 'admin') => {
  const index = productosData.findIndex(p => p.id === id);
  
  if (index !== -1) {
    productosData[index] = {
      ...productosData[index],
      ...datosActualizados,
      
      // NO sobrescribir autor y fechaCreacion originales
      autor: productosData[index].autor,
      fechaCreacion: productosData[index].fechaCreacion,
      
      // Actualizar solo estos campos
      fechaActualizacion: new Date().toISOString(),
      ultimoEditor: usuarioActual
    };
    return productosData[index];
  }
  
  return null;
};

export const eliminarProducto = (id) => {
  const index = productosData.findIndex(p => p.id === id);
  if (index !== -1) {
    return productosData.splice(index, 1)[0];
  }
  return null;
};

// Nueva función para ver historial
export const obtenerHistorialProducto = (id) => {
  const producto = obtenerProductoPorId(id);
  
  if (!producto) return null;
  
  return {
    id: producto.id,
    nombre: producto.nombre,
    creadoPor: producto.autor,
    fechaCreacion: producto.fechaCreacion,
    ultimaModificacion: producto.fechaActualizacion,
    modificadoPor: producto.ultimoEditor,
    diasDesdeCreacion: Math.floor(
      (new Date() - new Date(producto.fechaCreacion)) / (1000 * 60 * 60 * 24)
    )
  };
};